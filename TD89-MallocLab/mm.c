/* single word (4) or double word (8) alignment */
#define ALIGNMENT 8

/* rounds up to the nearest multiple of ALIGNMENT */
#define ALIGN(size) (((size) + (ALIGNMENT-1)) & ~0x7)


#define SIZE_T_SIZE (ALIGN(sizeof(size_t)))
#define HDRP(bp)       (void*)((char *)(bp) - SIZE_T_SIZE)
#define FTRP(bp)       (void*)((char *)(bp) + GET_SIZE(bp)) //line:vm:mm:ftrp

/* Read and write a word at address p */
#define GET(p)       (*(unsigned int *)(p))            //line:vm:mm:get
#define PUT(p, val)  (*(unsigned int *)(p) = (val))    //line:vm:mm:put



/* Read the size and allocated fields of bloc at address p */
#define GET_SIZE(p)  (GET(HDRP(p)) & ~0x7)                   
#define GET_ALLOC(p) (GET(HDRP(p)) & 0x1)                    

int* free_list_start = (int*)-1;
/* 
 * mm_init - initialize the malloc package.
 */
int mm_init(void)
{
	void* start_address = mem_heap_lo();
	printblock((void*)((char*)start_address + SIZE_T_SIZE));
	free_list_start = (void*) -1;
    return 0;
}

/* 
 * mm_malloc - Allocate a block by incrementing the brk pointer.
 *     Always allocate a block whose size is a multiple of the alignment.
 */
void *mm_malloc(size_t size)
{
    int newsize = ALIGN(size + SIZE_T_SIZE + SIZE_T_SIZE);
    void *p = mem_sbrk(newsize);
    if (p == (void *)-1)
	return NULL;
    else {
        *(size_t *)p = size;
        *(size_t *)((char *)p + (newsize-SIZE_T_SIZE)) = size;
        return (void *)((char *)p + SIZE_T_SIZE);
    }
}

/*
 * mm_free - Freeing a block adds it at the beginning of the free_list 
 */
void mm_free(void *ptr)
{
	// If free_list is empty, start it 
	if (free_list_start == (int*)-1){
		free_list_start = (int*) ptr;
	}
}




/*
 * mm_realloc - Implemented simply in terms of mm_malloc and mm_free
 */
void *mm_realloc(void *ptr, size_t size)
{
    void *oldptr = ptr;
    void *newptr;
    size_t copySize;
    
    newptr = mm_malloc(size);
    if (newptr == NULL)
      return NULL;
    copySize = *(size_t *)((char *)oldptr - SIZE_T_SIZE);
    if (size < copySize)
      copySize = size;
    memcpy(newptr, oldptr, copySize);
    mm_free(oldptr);
    return newptr;
}


